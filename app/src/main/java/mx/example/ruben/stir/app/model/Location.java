public class Location
{

    private String address;
    private String city;
    private String state;
    private double lat;
    private double lng;


    public String getAddress() {
        return address;
    }

    public String getCity() {
        return city;
    }

    public String getState() {
        return state;
    }

    public double getLat() {
        return lat;
    }

    public double getLng() {
        return lng;
    }

}